    private void pat_loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pat_loginActionPerformed
        // TODO add your handling code here:
        model = (DefaultTableModel) view_pat_encounter.getModel();
        model.setRowCount(0);
        
        if(pat_id.getText().isEmpty() || pat_pass.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "ID/Pass Cannot be Null");
        }
        else{
            if(parser.isLong(pat_id.getText()) || parser.isInteger(pat_id.getText()) ){
                patId = Long.parseLong(pat_id.getText());
                patPass = String.valueOf(pat_pass.getText());
                
                for(Patient p: patDir.getPatientList()){
                    if(patId == p.getId() && (p.getUserPass() == null ? patPass == null : p.getUserPass().equals(patPass))){
                        sel_pat = p;
                        logged_in = true;
                        JOptionPane.showMessageDialog(this, "Logged In!");
                    }
                }
                if(!logged_in){JOptionPane.showMessageDialog(this, "Please Check your creds");}
                else{
                    loginPage.setVisible(false);
                    encounterPage.setVisible(true);
                    
                    for(Encounter e:sel_pat.getPatientEncounterHistory()){
                        Object[] row = new Object[4];
                        row[0] = e.getLastVisitDate();
                        row[1] = e.getId();
                        row[2]=e.getVital().getBloodPressure();
                        row[3]= e.getVital().getSymptoms();
                        model.addRow(row);
                    }
                    
                }
            }
        }//end else
    }//GEN-LAST:event_pat_loginActionPerformed


